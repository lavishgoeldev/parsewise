"""Canonical source records for the personal-document sanity-check set.

Scope: 10 documents (2 per category) used as an *external validity* check
against the headline BEIR results. NOT the main benchmark — the main benchmark
is BEIR (FiQA, SciFact, HotpotQA, NFCorpus).

Each Source has:
  - id          : slot id (L001, I003, M002, IM001, T002)
  - category    : leases / invoices / medical / immigration / tax
  - title       : human-readable title
  - source_url  : where we'd download it from
  - publisher   : the source organization
  - license     : declared license; HUMAN-VERIFIED at download time
  - confidence  : 'url-confirmed' (URL+license both verified)
                  'needs-selection' (publisher confirmed, file picked at download)
                  'needs-license-verify' (URL works; eyeball license on source page)
  - doc_type    : free-text within-category type

This file is the *plan*. Actual files land on disk via download_corpus.py;
metadata is written from this file plus license-verification timestamps.
"""
